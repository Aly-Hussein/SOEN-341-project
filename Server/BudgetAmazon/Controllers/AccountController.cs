        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {

            Account existingUser = new Account();
            IEnumerable<AccountViewModel> listOfShoppingViewModels = (from objItem in objBudgetAmazonEntities.Accounts
                                                                      select new AccountViewModel()
                                                                      {
                                                                          Email = objItem.Email,
                                                                          Password = objItem.Password,
                                                                          IsAdmin = objItem.IsAdmin,
                                                                          CustomerId = objItem.CustomerId
                                                                      }
                                                                      ).ToList();

            foreach (AccountViewModel item in listOfShoppingViewModels)
            {
                if (item.Email == model.Email && item.Password == model.Password)
                {
                    existingUser.CustomerId = item.CustomerId;
                    existingUser.Email = item.Email;
                    existingUser.Password = item.Password;
                    existingUser.IsAdmin = item.IsAdmin;
                    if (item.IsAdmin == true)
                        Session["Admin"] = 1;
                    Session["log"] = item.CustomerId;
                    return RedirectToAction("Index", "Home");
                }

            }
            return View(model);
        }

      

       
